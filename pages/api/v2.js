  async function post(url,text,retries=0) {
    var config = {
        headers: {
            'Content-Type': 'text/plain'
        }
    };
    let currentTry =1;
    while(currentTry < retries){
      try {
        const res = await axios.post(url, text,config);
        console.log(`Status: ${res.status}`);
        console.log('Body: ', res.data);
        return res
    } catch (err) {
        console.log("Retry "+currentTry+++"/"+retries)
    }
    }
    return null
  }

  async function splitRequest(queryWords,lang){
    let lastIndex = 0;
    let results = []

    for(i=100;i<queryWords.length;i+=100){
      if(i>queryWords.lengh-1){
          let chunk = queryWords.slice(lastIndex,queryWords.lengh-1)
          let requestText = chunk.reduce((peviousValue,currentValue,currentIndex,array)=>{
              return peviousValue+=`\n${currentValue}`
          })
          //
          results.push(post(getUrl()+'?lang='+lang,requestText,10))
      }else{
          let chunk = queryWords.slice(lastIndex,i)
          lastIndex=i
          let requestText = chunk.reduce((peviousValue,currentValue,currentIndex,array)=>{
            return peviousValue+=`\n${currentValue}`
          })
          results.push(post(getUrl()+'?lang='+lang,requestText,10))
      }
    }
    let res = await Promise.all(results)
    console.log("Got all requests back...Merging")
    let mergedResults = mergeResults(res.filter(res=>res!=null).map(res=>res.data?res.data:{}))
           
    return mergedResults
  }

  function mergeResults(results){
        let mergedResults = results.reduce((previousValue, currentValue) => {
            Object.keys(currentValue).forEach((currentValue_key)=>{
                if(previousValue[currentValue_key]!=null &&previousValue[currentValue_key]!=undefined){
                    if(previousValue[currentValue_key].tradenames!=null){
                        previousValue[currentValue_key].tradenames= previousValue[currentValue_key].tradenames.concat(currentValue[currentValue_key].tradenames).unique()
                    }else{
                        previousValue[currentValue_key].tradenames = currentValue[currentValue_key].tradenames
                    }
                }else{
                    previousValue[currentValue_key]=currentValue[currentValue_key]
                }
            })
            return previousValue
        })
    
        return mergedResults
  }



