  function get(url) {
    return new Promise((resolve, reject) => {
      https.get(url, (resp) => {
        let data = '';
  
        resp.on('data', (chunk) => {
          data += chunk;
        });
        resp.on('end', () => {
          try {
            resolve(data)
          } catch (ex) {
            reject(data)
          }
        })
      })
    })
  }
  
